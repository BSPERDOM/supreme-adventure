package arreglo.nombres;

public class ArregloNombres {
    public static void main(String[] args) {
        String[] nombres = {
            "Juan",
            "María",
            "Pedro",
            "Lucía",
            "Carlos",
            "Ana",
            "Luis",
            "Elena",
            "Jorge",
            "Sofía"
        };
        
        for (String nombre : nombres) {
            System.out.println(nombre);
        }
    }
}

